def _generate_gemini_md(
    index_content: str, _project_name: str, cli_command: str
) -> str:
    milestones_match = re.search(
        r"## Active milestone\n([\s\S]*?)(?=\n##|$)", index_content
    )
    milestone = milestones_match.group(1).strip() if milestones_match else "(none)"

    now = datetime.now(UTC).isoformat()

    return f"""# MPGA-Managed Project Context \u2014 the GREATEST Context, Everybody Says So

This project uses MPGA \u2014 the GREATEST context engineering system ever built. Believe me.
Do NOT edit this file manually \u2014 generated from MPGA/INDEX.md.
Run `{cli_command} sync && {cli_command} export --antigravity` to update.

## Before starting any task \u2014 READ THIS FIRST, very important
1. Read MPGA/INDEX.md \u2014 this is the project's truth map, TREMENDOUS truth map
2. Find the relevant MPGA/scopes/*.md for the feature area
3. Check MPGA/board/BOARD.md for task state and assignments

## Evidence protocol \u2014 no FAKE claims allowed
- Every claim about code behavior must cite an evidence link
- Format: [E] src/auth/jwt.ts:42-67 :: validateToken()
- If you cannot verify a claim, mark it: [Unknown] description
- After modifying code, check if evidence links need updating

## TDD protocol (mandatory) \u2014 Uncle Bob's way. The ONLY way.
1. Write failing test FIRST
2. Implement minimal code to pass
3. Refactor without changing behavior
4. Update MPGA scope docs with new evidence links

## Parallel execution protocol \u2014 tremendous efficiency, the best efficiency
- One writer per scope at a time
- Read-only analysis can run in parallel
- Prefer milestone plans that isolate independent scopes into separate lanes

## Active milestone
{milestone}

## Verification commands \u2014 each one a WINNER
- `npm test` \u2014 run test suite
- `{cli_command} evidence verify` \u2014 check evidence links
- `{cli_command} drift --quick` \u2014 check for drift
- `{cli_command} board show` \u2014 view task board

We're going to Make This Project GREAT AGAIN!

Generated: {now}
"""


