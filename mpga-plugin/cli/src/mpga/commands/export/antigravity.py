from __future__ import annotations

import os
import re
from datetime import UTC, datetime
from pathlib import Path

from mpga.core.logger import log

from .agents import SKILL_NAMES, copy_skills_to
from .runtime import (
    copy_vendored_runtime,
    global_vendored_cli_command,
    project_vendored_cli_command,
)

# --- Antigravity export -------------------------------------------------------


def export_antigravity(
    project_root: str,
    mpga_dir: str,
    index_content: str,
    project_name: str,
    plugin_root: str | None,
    is_global: bool,
    opts: dict | None = None,
) -> None:
    if opts is None:
        opts = {}
    if is_global:
        home = os.environ.get("HOME", "~")
        gemini_root = str(Path(home) / ".gemini")
        cli_command = global_vendored_cli_command(gemini_root) if plugin_root else "mpga"
        copy_vendored_runtime(gemini_root, plugin_root)
        ag_global_skills_dir = str(
            Path(home) / ".gemini" / "antigravity" / "skills"
        )
        copy_skills_to(ag_global_skills_dir, plugin_root, "antigravity", cli_command)
        log.success(f"Generated ~/.gemini/antigravity/skills/ ({len(SKILL_NAMES)} skills)")
        ag_global_rules_dir = Path(home) / ".antigravity" / "rules"
        ag_global_rules_dir.mkdir(parents=True, exist_ok=True)
        (ag_global_rules_dir / "mpga-global.md").write_text(
            _generate_antigravity_global(cli_command), encoding="utf-8"
        )
        log.success("Generated ~/.antigravity/rules/mpga-global.md")
    else:
        cli_command = project_vendored_cli_command() if plugin_root else "mpga"
        copy_vendored_runtime(project_root, plugin_root)
        # GEMINI.md constitution
        (Path(project_root) / "GEMINI.md").write_text(
            _generate_gemini_md(index_content, project_name, cli_command),
            encoding="utf-8",
        )
        log.success("Generated GEMINI.md")
        # Skills in .agent/skills/
        ag_skills_dir = str(Path(project_root) / ".agent" / "skills")
        copy_skills_to(ag_skills_dir, plugin_root, "antigravity", cli_command)
        log.success(f".agent/skills/ ({len(SKILL_NAMES)} skills)")
        # Rules in .antigravity/rules/
        rules_dir = Path(project_root) / ".antigravity" / "rules"
        rules_dir.mkdir(parents=True, exist_ok=True)
        (rules_dir / "mpga-context.md").write_text(
            _generate_antigravity_context_md(index_content, project_name, cli_command),
            encoding="utf-8",
        )
        (rules_dir / "mpga-evidence.md").write_text(
            _generate_antigravity_evidence_md(cli_command), encoding="utf-8"
        )
        (rules_dir / "mpga-tdd.md").write_text(
            _generate_antigravity_tdd_md(cli_command), encoding="utf-8"
        )
        log.success("Generated .antigravity/rules/ (3 files)")
        # Workflows in .agents/workflows/ -- always generated for antigravity
        workflows_dir = Path(project_root) / ".agents" / "workflows"
        workflows_dir.mkdir(parents=True, exist_ok=True)
        (workflows_dir / "mpga-plan.md").write_text(
            _generate_antigravity_plan_workflow(cli_command), encoding="utf-8"
        )
        (workflows_dir / "mpga-develop.md").write_text(
            _generate_antigravity_develop_workflow(cli_command), encoding="utf-8"
        )
        (workflows_dir / "mpga-review.md").write_text(
            _generate_antigravity_review_workflow(cli_command), encoding="utf-8"
        )
        log.success("Generated .agents/workflows/ (3 workflow files)")
        # Knowledge Items seeding
        if opts.get("knowledge"):
            scopes_dir = Path(mpga_dir) / "scopes"
            if scopes_dir.exists():
                _seed_antigravity_knowledge_items(project_root, str(scopes_dir))
                log.success("Seeded Knowledge Items from MPGA/scopes/")


