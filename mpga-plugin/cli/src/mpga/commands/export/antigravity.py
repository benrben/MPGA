def _generate_antigravity_evidence_md(cli_command: str) -> str:
    return f"""# MPGA Evidence Protocol

## Evidence link format
```
[E] filepath:startLine-endLine :: symbolName()   # exact range + AST anchor (preferred)
[E] filepath :: symbolName                        # AST-only, resilient to refactors
[Unknown] description                             # explicitly unknown \u2014 never guess
[Stale:YYYY-MM-DD] filepath:range                # was valid, needs re-verification
```

## Rules
- EVERY claim about how code works must cite an evidence link
- When you cannot find evidence, write [Unknown] \u2014 never invent a claim
- After changing code, check whether evidence links in the affected scope still resolve
- Run `{cli_command} evidence heal --auto` to fix broken line ranges via AST

## Scope documents
Run `{cli_command} scope list` to see all scopes, then `{cli_command} scope show <name>` to read before touching code.
"""


def _generate_antigravity_tdd_md(cli_command: str) -> str:
    return f"""# MPGA TDD Protocol

## Mandatory test-driven development cycle

### For every code change:
1. RED: Write a failing test that describes expected behavior
   - Cite relevant evidence from `{cli_command} scope show <name>` in test comments
   - Run test \u2014 it MUST fail
2. GREEN: Write minimal implementation to make the test pass
   - YAGNI \u2014 do not add anything the test doesn't require
   - Run test \u2014 it MUST pass
3. BLUE: Refactor without changing behavior
   - All tests must stay green after every refactoring step
   - Update evidence links in the scope if code moved

### Rules
- NEVER write implementation code before a test exists
- NEVER add features not covered by tests
- ALWAYS update task cards with TDD stage progress (run `{cli_command} board show`)
"""


