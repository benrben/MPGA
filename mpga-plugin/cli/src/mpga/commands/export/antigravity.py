def _generate_antigravity_plan_workflow(cli_command: str) -> str:
    return f"""# MPGA Plan Workflow

## Trigger
Use this workflow when a milestone has been created and needs an implementation plan.

## Steps
1. Run `{cli_command} status` for project map and scope registry
2. Run `{cli_command} milestone show` to identify the active milestone
3. Run `{cli_command} scope show <name>` for relevant scope docs
4. Run research and read-only scope discovery in parallel where possible
5. Break work into tasks (2-10 min each)
   - Each task MUST cite exact files to modify (with evidence links)
   - Each task MUST specify expected test file locations
   - Each task MUST have mechanically verifiable acceptance criteria
   - Mark which tasks can run in parallel by scope
6. Add tasks to board: `{cli_command} board add "<title>" --scope <name>`
"""


def _generate_antigravity_develop_workflow(cli_command: str) -> str:
    return f"""# MPGA Develop Workflow

## Trigger
Use this workflow when implementing a planned task from the MPGA board.

## Steps
1. Run `{cli_command} board show` to find the task
2. Run `{cli_command} scope show <name>` for each scope listed in the task
3. Claim a scope-local write lane \u2014 one writer per scope
4. Write failing test (cite scope evidence in comments)
5. Implement minimal code to pass
6. Refactor for quality
7. Let read-only scout/auditor checks run in parallel while you write
8. Update evidence links in the relevant scope
9. Update the task card: set tdd_stage, add evidence_produced
10. Move task to next board column
11. Run: `{cli_command} drift --quick`
12. Commit with conventional message
"""


def _generate_antigravity_review_workflow(cli_command: str) -> str:
    return f"""# MPGA Review Workflow

## Trigger
Use this workflow when a task reaches the review column.

## Stage 1 \u2014 Spec compliance
- Does implementation match the milestone plan? (run `{cli_command} milestone show`)
- Are all evidence links in scope docs still valid? (`{cli_command} evidence verify`)
- Were tests written BEFORE implementation? (check git log order)

## Stage 2 \u2014 Code quality
- Clean code principles (naming, single responsibility, DRY)
- Error handling for expected failure modes
- No security regressions
- Performance considerations

## Output
- PASS: move task to done, update board
- FAIL (critical): move task back to in-progress, note the blocker
- FAIL (warning): move to done with follow-up task created
"""


